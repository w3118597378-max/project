<template>
	<div>
		<div class="center_view edit_form">
			<el-form class="userinfo_form" ref="userinfoFormRef" :model="form">
				<el-row>
					<el-col :span="12">
						<el-form-item label="队长账号" prop="duizhangzhanghao">
							<el-input class="list_inp" v-model="user.duizhangzhanghao" readonly placeholder="队长账号" clearable />
						</el-form-item>
					</el-col>
					<el-col :span="12">
						<el-form-item label="队长姓名" prop="duizhangxingming">
							<el-input class="list_inp" v-model="user.duizhangxingming"  placeholder="队长姓名" clearable />
						</el-form-item>
					</el-col>
					<el-col :span="12">
						<el-form-item label="照片" prop="zhaopian">
							<uploads
								action="file/upload" 
								tip="请上传照片"
								style="width: 100%;text-align: left;"
								:fileUrls="user.zhaopian?user.zhaopian:''" 
								@change="qiuduiduizhangzhaopianUploadSuccess">
							</uploads>
						</el-form-item>
					</el-col>
					<el-col :span="12">
						<el-form-item label="性别" prop="xingbie">
							<el-select 
								class="list_sel" 
								v-model="user.xingbie" 
								placeholder="请选择性别"
								>
								<el-option v-for="item in qiuduiduizhangxingbieLists" :label="item" :value="item"></el-option>
							</el-select>
						</el-form-item>
					</el-col>
					<el-col :span="12">
						<el-form-item label="电话" prop="dianhua">
							<el-input class="list_inp" v-model="user.dianhua"  placeholder="电话" clearable />
						</el-form-item>
					</el-col>
					<el-col :span="12">
						<el-form-item label="球队名称" prop="qiuduimingcheng">
							<el-select 
								class="list_sel" 
								v-model="user.qiuduimingcheng" 
								placeholder="请选择球队名称"
								:disabled="true"
								>
								<el-option v-for="item in qiuduiduizhangqiuduimingchengLists" :label="item" :value="item"></el-option>
							</el-select>
						</el-form-item>
					</el-col>
					<span class="formModel_btn_box">
						<el-button class='confirm_btn' type="primary" @click="onSubmit">保存</el-button>
					</span>
				</el-row>
			</el-form>
		</div>
	</div>
</template>

<script setup>
	import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/toolUtil";
	import {
		reactive,
		ref,
		getCurrentInstance,
        computed
	} from 'vue'
	import { useStore } from 'vuex'
	const store = useStore()
	const context = getCurrentInstance()?.appContext.config.globalProperties;
	const tableName = ref('qiuduiduizhang')
	const user = ref({})
	const qiuduiduizhangxingbieLists = ref([])
	const qiuduiduizhangqiuduimingchengLists = ref([])
	const init = () => {
		qiuduiduizhangxingbieLists.value = "男,女".split(',')
		context?.$http({
			url:`option/qiuduipaixingbang/qiuduimingcheng`,
			method:'get',
		}).then(res=>{
			qiuduiduizhangqiuduimingchengLists.value = res.data.data
		})
	}
	const qiuduiduizhangzhaopianUploadSuccess=(fileUrls)=> {
	    user.value.zhaopian = fileUrls;
	}
	const onSubmit = () => {
		if((!user.value.duizhangzhanghao)){
			context?.$toolUtil.message(`队长账号不能为空`,'error')
			return false
		}
		if((!user.value.mima)){
			context?.$toolUtil.message(`密码不能为空`,'error')
			return false
		}
		if(user.value.zhaopian!=null){
			user.value.zhaopian = user.value.zhaopian.replace(new RegExp(context?.$config.url,"g"),"");
		}
		if((user.value.dianhua)&&(!context?.$toolUtil.isMobile(user.value.dianhua))){
			context?.$toolUtil.message(`电话应输入手机格式`,'error')
			return false
		}
		store.dispatch('user/update',user.value).then(res=>{
			if(res?.data&&res.data.code==0)context?.$toolUtil.message('修改成功','success')
		})

	}
	const getInfo = () => {
		context?.$http({
			url: `${tableName.value}/session`,
			method: 'get'
		}).then(res => {
			user.value = res.data.data
			init()
		})
	}
	getInfo()
</script>

